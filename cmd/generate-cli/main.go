func writeCommandEntry(file *os.File, cmdName string, commandConfig *pkgConfig.CommandConfig) {
	cmd := commandConfig.Commands[cmdName]
	constName := "Command" + toPascalCase(cmdName)

	_, _ = fmt.Fprintf(file, "\t%s: {\n", constName)
	_, _ = fmt.Fprintf(file, "\t\tName: %s,\n", constName)
	_, _ = fmt.Fprintf(file, "\t\tCategory: %q,\n", cmd.Category)
	_, _ = fmt.Fprintf(file, "\t\tHandler: %q,\n", cmd.Handler)
	_, _ = fmt.Fprintf(file, "\t\tDescription: %q,\n", escapeString(cmd.Description))

	flags := getCommandFlags(cmdName, commandConfig)
	writeCommandFlags(file, flags)

	_, _ = fmt.Fprintf(file, "\t},\n")
}

func getCommandFlags(cmdName string, commandConfig *pkgConfig.CommandConfig) []string {
	var flags []string
	cmd := commandConfig.Commands[cmdName]

	for flagName := range cmd.Flags {
		flags = append(flags, "Flag"+toPascalCase(flagName))
	}
	for flagName := range commandConfig.Global.Flags {
		flags = append(flags, "Flag"+toPascalCase(flagName))
	}

	sort.Strings(flags)
	return flags
}

