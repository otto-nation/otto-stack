func writeMessageConstants(file *os.File, messageMetadata map[string]string) {
	if len(messageMetadata) == 0 {
		return
	}

	_, _ = fmt.Fprintln(file, "// Message key constants")
	_, _ = fmt.Fprintln(file, "const (")

	for msgKey := range messageMetadata {
		constName := "Msg" + toPascalCase(strings.ReplaceAll(msgKey, ".", "_"))
		_, _ = fmt.Fprintf(file, "\t%s = %q\n", constName, msgKey)
	}
	_, _ = fmt.Fprintln(file, ")")
	_, _ = fmt.Fprintln(file)
}

