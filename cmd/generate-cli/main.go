type FlagMetadata struct {
	Name        string
	Type        string
	Short       string
	Description string
	Default     any
	Options     []string
	Commands    []string
	IsGlobal    bool
}

func main() {
	commandConfig, err := loadConfig()
	if err != nil {
		_, _ = fmt.Fprintf(os.Stderr, "Failed to load config: %v\n", err)
		os.Exit(1)
	}

	// Template-based generation (disabled for now to avoid conflicts)
	// if err := generateWithTemplates(commandConfig); err != nil {
	// 	_, _ = fmt.Fprintf(os.Stderr, "Failed to generate with templates: %v\n", err)
	// 	os.Exit(1)
	// }

	// Keep legacy generation for now (can be removed later)
	flagMetadata := collectFlagMetadata(commandConfig)
	messageMetadata := collectMessageMetadata(commandConfig)
	iconMetadata := collectIconMetadata(commandConfig)

	if err := generateCLIFile(flagMetadata, messageMetadata, iconMetadata, commandConfig); err != nil {
		_, _ = fmt.Fprintf(os.Stderr, "Failed to generate CLI file: %v\n", err)
		os.Exit(1)
	}
}

func loadConfig() (*pkgConfig.CommandConfig, error) {
	var commandConfig pkgConfig.CommandConfig
	if err := yaml.Unmarshal(config.EmbeddedCommandsYAML, &commandConfig); err != nil {
		return nil, fmt.Errorf("failed to parse commands.yaml: %w", err)
	}
	return &commandConfig, nil
}

func collectFlagMetadata(commandConfig *pkgConfig.CommandConfig) map[string]*FlagMetadata {
	flagMetadata := make(map[string]*FlagMetadata)

	// Process global flags
	for flagName, flag := range commandConfig.Global.Flags {
		flagMetadata[flagName] = &FlagMetadata{
			Name:        flagName,
			Type:        flag.Type,
			Short:       flag.Short,
			Description: flag.Description,
			Default:     flag.Default,
			Options:     flag.Options,
			Commands:    []string{},
			IsGlobal:    true,
		}
	}

	// Process command-specific flags
	for cmdName, command := range commandConfig.Commands {
		for flagName, flag := range command.Flags {
			if existing, exists := flagMetadata[flagName]; exists {
				existing.Commands = append(existing.Commands, cmdName)
			} else {
				flagMetadata[flagName] = &FlagMetadata{
					Name:        flagName,
					Type:        flag.Type,
					Short:       flag.Short,
					Description: flag.Description,
					Default:     flag.Default,
					Options:     flag.Options,
					Commands:    []string{cmdName},
					IsGlobal:    false,
				}
			}
		}
	}

	// Sort commands for consistent output
	for _, metadata := range flagMetadata {
		sort.Strings(metadata.Commands)
	}

	return flagMetadata
}

func generateCLIFile(flagMetadata map[string]*FlagMetadata, messageMetadata map[string]string, iconMetadata map[string]string, commandConfig *pkgConfig.CommandConfig) error {
	if len(flagMetadata) == 0 {
		return fmt.Errorf("no flags found in configuration")
	}

	file, err := os.Create(CLIOutputPath)
	if err != nil {
		return fmt.Errorf("failed to create CLI file: %w", err)
	}
	defer func() { _ = file.Close() }()

	writeFileHeader(file)
	writeFlagStruct(file)
	writeFlagConstants(file, flagMetadata)
	writeMessageConstants(file, messageMetadata)
	writeCommandConstants(file, commandConfig)
	writeFlagMetadataMap(file, flagMetadata)
	writeMessageMetadataMap(file, messageMetadata)
	writeIconMetadataMap(file, iconMetadata)
	writeCommandMetadataMap(file, commandConfig)
	writeHelperFunctions(file)

	// Add parsers to same file
	for cmdName, cmd := range commandConfig.Commands {
		writeCommandParser(file, cmdName, cmd, commandConfig)
	}

	fmt.Printf("Generated CLI code for %d flags and %d commands\n", len(flagMetadata), len(commandConfig.Commands))
	return nil
}

