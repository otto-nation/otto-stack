func writeFlagMetadataMap(file *os.File, flagMetadata map[string]*FlagMetadata) {
	_, _ = fmt.Fprintln(file, "// Flags contains metadata for all flags from commands.yaml")
	_, _ = fmt.Fprintln(file, "var Flags = map[string]FlagInfo{")

	flagNames := getSortedFlagNames(flagMetadata)
	for _, flagName := range flagNames {
		metadata := flagMetadata[flagName]
		constName := "Flag" + toPascalCase(flagName)
		writeFlagEntry(file, constName, metadata)
	}
	_, _ = fmt.Fprintln(file, "}")
	_, _ = fmt.Fprintln(file)
}

func writeFlagEntry(file *os.File, constName string, metadata *FlagMetadata) {
	_, _ = fmt.Fprintf(file, "\t%s: {\n", constName)
	_, _ = fmt.Fprintf(file, "\t\tName:        %s,\n", constName)
	_, _ = fmt.Fprintf(file, "\t\tType:        %q,\n", metadata.Type)
	_, _ = fmt.Fprintf(file, "\t\tShort:       %q,\n", metadata.Short)
	_, _ = fmt.Fprintf(file, "\t\tDescription: %q,\n", escapeString(metadata.Description))

	writeDefaultValue(file, metadata.Default)
	writeOptions(file, metadata.Options)
	writeCommands(file, metadata.Commands)

	_, _ = fmt.Fprintf(file, "\t\tIsGlobal:    %t,\n", metadata.IsGlobal)
	_, _ = fmt.Fprintf(file, "\t},\n")
}

func writeDefaultValue(file *os.File, defaultVal any) {
	if defaultVal == nil {
		_, _ = fmt.Fprintf(file, "\t\tDefault:     nil,\n")
		return
	}

	switch v := defaultVal.(type) {
	case bool:
		_, _ = fmt.Fprintf(file, "\t\tDefault:     %t,\n", v)
	case string:
		_, _ = fmt.Fprintf(file, "\t\tDefault:     %q,\n", v)
	case int:
		_, _ = fmt.Fprintf(file, "\t\tDefault:     %d,\n", v)
	case float64:
		if v == float64(int(v)) {
			_, _ = fmt.Fprintf(file, "\t\tDefault:     %d,\n", int(v))
		} else {
			_, _ = fmt.Fprintf(file, "\t\tDefault:     %g,\n", v)
		}
	default:
		_, _ = fmt.Fprintf(file, "\t\tDefault:     nil,\n")
	}
}

func writeOptions(file *os.File, options []string) {
	if len(options) == 0 {
		_, _ = fmt.Fprintf(file, "\t\tOptions:     nil,\n")
		return
	}

	_, _ = fmt.Fprintf(file, "\t\tOptions:     []string{")
	for i, option := range options {
		if i > 0 {
			_, _ = fmt.Fprintf(file, ", ")
		}
		_, _ = fmt.Fprintf(file, "%q", option)
	}
	_, _ = fmt.Fprintf(file, "},\n")
}

