func toPascalCase(s string) string {
	// Handle special cases
	switch s {
	case "json":
		return "JSON"
	case "tty":
		return "TTY"
	}

	parts := strings.Split(s, "-")
	for i, part := range parts {
		if len(part) > 0 {
			// Handle special acronyms
			switch strings.ToLower(part) {
			case "json":
				parts[i] = "JSON"
			case "tty":
				parts[i] = "TTY"
			default:
				parts[i] = strings.ToUpper(part[:1]) + strings.ToLower(part[1:])
			}
		}
	}
	return strings.Join(parts, "")
}

// escapeString escapes quotes and newlines in strings for Go code generation
func escapeString(s string) string {
	s = strings.ReplaceAll(s, "\\", "\\\\")
	s = strings.ReplaceAll(s, "\"", "\\\"")
	s = strings.ReplaceAll(s, "\n", "\\n")
	s = strings.ReplaceAll(s, "\r", "\\r")
	s = strings.ReplaceAll(s, "\t", "\\t")
	return s
}

func writeCommandParser(file *os.File, cmdName string, cmd pkgConfig.Command, config *pkgConfig.CommandConfig) {
	structName := toPascalCase(cmdName) + "Flags"
	funcName := "Parse" + toPascalCase(cmdName) + "Flags"

	flagFields := collectCommandFlags(cmd, config)

	writeParserStruct(file, structName, flagFields)
	writeParserFunction(file, funcName, structName, flagFields)
}

func writeParserStruct(file *os.File, structName string, flagFields []flagField) {
	_, _ = fmt.Fprintf(file, "type %s struct {\n", structName)
	for _, field := range flagFields {
		_, _ = fmt.Fprintf(file, "\t%s %s `flag:\"%s\"`\n", field.Name, field.Type, field.FlagName)
		if field.FlagName == "timeout" && field.YAMLType == constants.TypeInt {
			_, _ = fmt.Fprintf(file, "\t%sDuration time.Duration\n", field.Name)
		}
	}
	_, _ = fmt.Fprintln(file, "}")
	_, _ = fmt.Fprintln(file)
}

func writeParserFunction(file *os.File, funcName, structName string, flagFields []flagField) {
	_, _ = fmt.Fprintf(file, "func %s(cmd *cobra.Command) (*%s, error) {\n", funcName, structName)
	_, _ = fmt.Fprintf(file, "\tflags := &%s{}\n", structName)
	_, _ = fmt.Fprintln(file, "\tif err := parseFlags(cmd, flags); err != nil {")
	_, _ = fmt.Fprintln(file, "\t\treturn nil, err")
	_, _ = fmt.Fprintln(file, "\t}")
	_, _ = fmt.Fprintln(file)

	for _, field := range flagFields {
		if field.FlagName == "timeout" && field.YAMLType == constants.TypeInt {
			_, _ = fmt.Fprintf(file, "\tflags.%sDuration = time.Duration(flags.%s) * time.Second\n", field.Name, field.Name)
		}
	}

	_, _ = fmt.Fprintln(file, "\treturn flags, nil")
	_, _ = fmt.Fprintln(file, "}")
	_, _ = fmt.Fprintln(file)
}

func writeSimpleFlagParsing(file *os.File, field flagField) {
	constName := "Flag" + toPascalCase(field.FlagName)
	switch field.YAMLType {
	case constants.TypeBool:
		_, _ = fmt.Fprintf(file, "\tflags.%s, err = getBoolFlag(cmd, %s)\n", field.Name, constName)
	case constants.TypeString:
		_, _ = fmt.Fprintf(file, "\tflags.%s, err = getStringFlag(cmd, %s)\n", field.Name, constName)
	case constants.TypeInt:
		_, _ = fmt.Fprintf(file, "\tflags.%s, err = getIntFlag(cmd, %s)\n", field.Name, constName)
	}
	_, _ = fmt.Fprintln(file, "\tif err != nil {")
	_, _ = fmt.Fprintln(file, "\t\treturn nil, err")
	_, _ = fmt.Fprintln(file, "\t}")
	_, _ = fmt.Fprintln(file)
}

func collectCommandFlags(cmd pkgConfig.Command, config *pkgConfig.CommandConfig) []flagField {
	var flagFields []flagField

	for flagName, flag := range cmd.Flags {
		flagFields = append(flagFields, flagField{
			Name:     toPascalCase(flagName),
			Type:     goTypeFromYAML(flag.Type),
			FlagName: flagName,
			YAMLType: flag.Type,
		})
	}

	for flagName, flag := range config.Global.Flags {
		if !flagExists(flagFields, flagName) {
			flagFields = append(flagFields, flagField{
				Name:     toPascalCase(flagName),
				Type:     goTypeFromYAML(flag.Type),
				FlagName: flagName,
				YAMLType: flag.Type,
			})
		}
	}

	return flagFields
}

func flagExists(fields []flagField, flagName string) bool {
	for _, field := range fields {
		if field.FlagName == flagName {
			return true
		}
	}
	return false
}

func writeFlagParsing(file *os.File, field flagField) {
	constName := "Flag" + toPascalCase(field.FlagName)
	switch field.YAMLType {
	case constants.TypeBool:
		_, _ = fmt.Fprintf(file, "\tif flags.%s, err = cmd.Flags().GetBool(%s); err != nil {\n", field.Name, constName)
	case constants.TypeString:
		_, _ = fmt.Fprintf(file, "\tif flags.%s, err = cmd.Flags().GetString(%s); err != nil {\n", field.Name, constName)
	case constants.TypeInt:
		_, _ = fmt.Fprintf(file, "\tif flags.%s, err = cmd.Flags().GetInt(%s); err != nil {\n", field.Name, constName)
	}
	_, _ = fmt.Fprintf(file, "\t\treturn nil, fmt.Errorf(\"failed to get %s flag: %%w\", err)\n", field.FlagName)
	_, _ = fmt.Fprintln(file, "\t}")
	_, _ = fmt.Fprintln(file)
}

